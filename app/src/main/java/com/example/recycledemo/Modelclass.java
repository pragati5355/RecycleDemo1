package com.example.recycledemo;

public class Modelclass {

    String TxtName;
    String TxtMob;
    String TxtMail;
    String TxtWeb;

    public Modelclass(){
        this.TxtName=TxtName;
        this.TxtMob=TxtMob;
        this.TxtMail=TxtMail;
        this.TxtWeb=TxtWeb;
    }

    public String getTxtName() {
        return TxtName;
    }

    public void setTxtName(String txtName) {
        TxtName = txtName;
    }

    public String getTxtMob() {
        return TxtMob;
    }

    public void setTxtMob(String txtMob) {
        TxtMob = txtMob;
    }

    public String getTxtMail() {
        return TxtMail;
    }

    public void setTxtMail(String txtMail) {
        TxtMail = txtMail;
    }

    public String getTxtWeb() {
        return TxtWeb;
    }

    public void setTxtWeb(String txtWeb) {
        TxtWeb = txtWeb;
    }
}
